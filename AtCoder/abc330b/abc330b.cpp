void solve() {
    int n, l, r; cin >> n >> l >> r;
    
    for (int i = 0; i < n; ++i) {
        int q; cin >> q;
        if (q <= l) cout << l << " ";
        else if (q >= r) cout << r << " ";
        else cout << q << " ";
    }
    cout << en;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}