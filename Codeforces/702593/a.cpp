template<typename T>
istream& operator>>(istream& in, vector<T>& a) {
    for (T& i : a) 
        in >> i;
    return in;
}

template<typename T>
ostream& operator<<(ostream& out, const vector<T>& a) {
    for (const auto& i : a) { 
        out << i << " ";
    }
    return out;
}

ll binpow(ll a, ll b, ll MOD) {
    a %= MOD;
    ll res = 1;
    while (b) {
        if (b & 1)
            res = (res * a) % MOD;
        a = (a * a) % MOD;
        b >>= 1;
    }
    return res;
}

void solve() {
    ll n, k; cin >> n >> k;

    int cnt = 0;
    if (n % 2)
        cnt = (k + 1) / 2;
    else
        cnt = k / 2;

    if (cnt % 2 == 0)
        cout << "yes" << en;
    else
        cout << "no" << en;
}

